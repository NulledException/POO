public class Matematica {

    public Matematica() {
    }

    public double soma(int x, int y) {
        return (x + y);
    }

    public double soma(int x, double y) {
        return (x + y);
    }

    public double soma(double x, double y) {
        return (x + y);
    }

    public double soma(double x, int y) {
        return (x + y);
    }

    public double sub(int x, int y) {
        return (x - y);
    }

    public double sub(int x, double y) {
        return (x - y);
    }

    public double sub(double x, double y) {
        return (x - y);
    }

    public double sub(double x, int y) {
        return (x - y);
    }

}